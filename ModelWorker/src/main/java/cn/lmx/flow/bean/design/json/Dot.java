package cn.lmx.flow.bean.design.json;

public class Dot {
	private int x;
	private int y;
	/**
	 * 构造函数
	 */
	public Dot() {
		
	}
	public final int getX() {
		return x;
	}
	public final void setX(int x) {
		this.x = x;
	}
	public final int getY() {
		return y;
	}
	public final void setY(int y) {
		this.y = y;
	}
}
