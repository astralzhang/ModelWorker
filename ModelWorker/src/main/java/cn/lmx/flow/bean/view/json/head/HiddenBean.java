package cn.lmx.flow.bean.view.json.head;

public class HiddenBean {
	//field
	private String field;
	//是否保存
	private String save;
	/**
	 * 构造函数
	 */
	public HiddenBean() {
		
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	public String getSave() {
		return save;
	}
	public void setSave(String save) {
		this.save = save;
	}
}
