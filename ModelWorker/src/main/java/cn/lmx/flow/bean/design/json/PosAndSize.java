package cn.lmx.flow.bean.design.json;

public class PosAndSize {
	private int x;
	private int y;
	private int width;
	private int heght;
	/**
	 * 构造函数
	 */
	public PosAndSize() {
		
	}
	public final int getX() {
		return x;
	}
	public final void setX(int x) {
		this.x = x;
	}
	public final int getY() {
		return y;
	}
	public final void setY(int y) {
		this.y = y;
	}
	public final int getWidth() {
		return width;
	}
	public final void setWidth(int width) {
		this.width = width;
	}
	public final int getHeght() {
		return heght;
	}
	public final void setHeght(int heght) {
		this.heght = heght;
	}
}
