package cn.lmx.flow.bean.SQL;

public class FieldBean {
	//字段ID
	private String id;
	//字段名称
	private String name;
	/**
	 * 构造函数
	 */
	public FieldBean() {
		
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
